package AtClass;

public class Class6 {
    public static void main(String[] args) {
        String str1 = "Java is a programming language";
        String str2 = "Java is a amazing";


        System.out.println(str1.length()); // длина
        System.out.println(str1.toLowerCase()); // прописные abcd
        System.out.println(str1.toUpperCase()); // заглавные ABCD
        System.out.println(str1.equals(str2));
        System.out.println(str1.equalsIgnoreCase(str2));
        System.out.println(str1.charAt(2));
        System.out.println(str1.substring(0, 6));

    }


}
